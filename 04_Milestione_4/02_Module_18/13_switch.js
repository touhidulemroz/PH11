var theme = 'dark';
switch (theme) {
    case 'light':
        console.log('light theme');
        break;
    case 'dark':
        console.log('dark theme');
        break;
    case 'red':
        console.log('red theme');
        break;
    case 'blue':
        console.log('blue theme');
        break;
    default:
        console.log('no theme');
        break;
}